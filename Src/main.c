/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */
  

  /* MCU Configuration--------------------------------------------------------*/
	RCC->APB2ENR |= RCC_APB2ENR_SYSCFGEN;
	SYSCFG->CFGR1 = (uint32_t)0x00000000;
  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */
	SysTick->CTRL |= SysTick_CTRL_TICKINT_Msk;
  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_USART1_UART_Init();
  MX_USART2_UART_Init();
  MX_USART3_UART_Init();
  MX_USART4_UART_Init();
  MX_USART5_UART_Init();
  MX_USART6_UART_Init();
  MX_USART7_UART_Init();
  MX_USART8_UART_Init();
  MX_TIM1_Init();
  /* USER CODE BEGIN 2 */
  HAL_GPIO_WritePin(LED1_GPIO_Port,LED1_Pin,1);
  HAL_GPIO_WritePin(LED2_GPIO_Port,LED2_Pin,1);
  HAL_Delay(1000);
  HAL_GPIO_WritePin(LED2_GPIO_Port,LED2_Pin,0);
	init_cmdlink();
	Init_InterfacNode();
	HAL_Delay(100);	
	BootConfig_User();
	async_write(5,startmsg,5);
  /* USER CODE END 2 */


  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    /* USER CODE END WHILE */
    Get_UpperCommand(dataBuffer);
    if(Get_DeviceCommand(0,dataBuffer))
    {
      Send_UpperCommand(dataBuffer);
    }

    if(Get_DeviceCommand(1,dataBuffer))
    {
      Send_UpperCommand(dataBuffer);
    }

    if(Get_DeviceCommand(2,dataBuffer))
    {
      Send_UpperCommand(dataBuffer);
    }

    if(Get_DeviceCommand(3,dataBuffer))
    {
      Send_UpperCommand(dataBuffer);
    }
    if(Get_DeviceCommand(4,dataBuffer))
    {
      Send_UpperCommand(dataBuffer);
    }
    Get_LowerCommand(dataBuffer);
    if((LL_GPIO_ReadInputPort(MI_GPIO_Port)& MI_Pin) != 0)
    {
      // switch to directly link position
      Enable_DirectLink();	
      // to detect MI pin until it turns low
      while(1)
      {
        if((LL_GPIO_ReadInputPort(MI_GPIO_Port) & MI_Pin) == 0)
        {
          // check if the signal is steable 
          HAL_Delay(200);
          if((LL_GPIO_ReadInputPort(MI_GPIO_Port) & MI_Pin) == 0)
          {
            Disable_DirectLink();
            break;
          }
        }
      }
    }
    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

/**
  * @brief System Clock Configuration
  * @retval None
  */
void SystemClock_Config(void)
{
  LL_FLASH_SetLatency(LL_FLASH_LATENCY_1);

  if(LL_FLASH_GetLatency() != LL_FLASH_LATENCY_1)
  {
  Error_Handler();  
  }
  LL_RCC_HSI_Enable();

   /* Wait till HSI is ready */
  while(LL_RCC_HSI_IsReady() != 1)
  {
    
  }
  LL_RCC_HSI_SetCalibTrimming(16);
  LL_RCC_PLL_ConfigDomain_SYS(LL_RCC_PLLSOURCE_HSI, LL_RCC_PLL_MUL_12, LL_RCC_PREDIV_DIV_2);
  LL_RCC_PLL_Enable();

   /* Wait till PLL is ready */
  while(LL_RCC_PLL_IsReady() != 1)
  {
    
  }
  LL_RCC_SetAHBPrescaler(LL_RCC_SYSCLK_DIV_1);
  LL_RCC_SetAPB1Prescaler(LL_RCC_APB1_DIV_1);
  LL_RCC_SetSysClkSource(LL_RCC_SYS_CLKSOURCE_PLL);

   /* Wait till System clock is ready */
  while(LL_RCC_GetSysClkSource() != LL_RCC_SYS_CLKSOURCE_STATUS_PLL)
  {
  
  }
  LL_Init1msTick(48000000);
  LL_SYSTICK_SetClkSource(LL_SYSTICK_CLKSOURCE_HCLK);
  LL_SetSystemCoreClock(48000000);
  LL_RCC_SetUSARTClockSource(LL_RCC_USART1_CLKSOURCE_PCLK1);
  LL_RCC_SetUSARTClockSource(LL_RCC_USART2_CLKSOURCE_PCLK1);
  LL_RCC_SetUSARTClockSource(LL_RCC_USART3_CLKSOURCE_PCLK1);
}

